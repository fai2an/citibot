sendForm.onkeydown = function(e){
  if(e.keyCode == 13){
    e.preventDefault();

    //No mix ups with upper and lowercases
    var input = textInput.value.toLowerCase();

    //Empty textarea fix
    if(input.length > 0) {
      createBubble(input)
    }
  }
};

sendForm.addEventListener('submit', function(e) {
  //so form doesnt submit page (no page refresh)
  e.preventDefault();

  //No mix ups with upper and lowercases
  var input = textInput.value.toLowerCase();

  //Empty textarea fix
  if(input.length > 0) {
    createBubble(input)
  }
}) //end of eventlistener

var createBubble = function(input) {
  //create input bubble
  var chatBubble = document.createElement('li');
  chatBubble.classList.add('userInput');

  //adds input of textarea to chatbubble list item
  chatBubble.innerHTML = input;

  //adds chatBubble to chatlist
  chatList.appendChild(chatBubble)

  bot_response(input);
}

const bot_response = function(input){
  url = "http://127.0.0.1:8000/search="
  console.log(url.concat(input))
  fetch(url.concat(input))
  .then((data)=>{return data.json()})
  .then((res)=>{create_response(res)})
  
}

var create_response = function(text) {
  var userBubble = document.createElement('li');
  userBubble.classList.add('bot__output');
  for(let i =0;i<text.length;i++){
  userBubble.innerHTML = page_details(text[i])
  chatList.appendChild(userBubble)
  }
  animationCounter = 1
  textInput.value = "";
}

